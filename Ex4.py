print('Calculo da nota bimestral do aluno\n')
nota1 = float(input('Entre com a primeira nota: '))
nota2 = float(input('Entre com o segunda nota: '))
nota3 = float(input('Entre com a terceira nota: '))
nota4 = float(input('Entre coma terceira nota: '))
media = (nota1 + nota2 + nota3 + nota4)/4
print(f'A nota final do aluno foi: {media:.2f}')
